package com.chorus.exceptions;

public class UsuarioConfirmacaoSenhaException extends Exception {

	private static final long serialVersionUID = -383548362592693137L;

	public UsuarioConfirmacaoSenhaException() {
		super();
	}

	public UsuarioConfirmacaoSenhaException(String message, Throwable cause) {
		super(message, cause);
	}

	public UsuarioConfirmacaoSenhaException(String message) {
		super(message);
	}

	public UsuarioConfirmacaoSenhaException(Throwable cause) {
		super(cause);
	}

}
